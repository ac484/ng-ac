/**
 * 取得錯誤訊息
 */
export function getErrorMessage(code: ErrorCode, language: Language = 'zh-TW'): string {
  return ERROR_MESSAGES[language]?.[code] || ERROR_MESSAGES['zh-TW'][code] || code;
}

/**
 * 取得多語言錯誤訊息
 */
export function getMultiLanguageErrorMessage(code: ErrorCode, languages: Language[] = ['zh-TW']): Record<Language, string> {
  const result: Record<Language, string> = {} as Record<Language, string>;

  languages.forEach(lang => {
    result[lang] = getErrorMessage(code, lang);
  });

  return result;
}
